'''
int 整型
float 浮点类型
complex 复数
bool 布尔类型
'''

var = 521  # <class 'int'>

var = 3.1415926  # <class 'float'>

var = 0x10  # <class 'int'> 十六进制的 16

var = b'001100111'  # <class 'bytes'> 二进制表示需要使用b'中间是二进制数'

var = 5+6j  # <class 'complex'>

# 布尔类型
var = True  # <class 'bool'>
var = False  # <class 'bool'>

# print(var, type(var))

# 数值类型可以参与运算
a = 10
b = 20
print(a+b)


